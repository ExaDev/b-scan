    /**
     * Get 5-digit SKU for a Shopify variant ID
     * @param variantId Shopify store variant ID (e.g., "40862518935612")
     * @return 5-digit SKU (e.g., "15200") or null if not found
     */
    fun getSkuByVariantId(variantId: String): String? {
        return shopifyVariantToSku[variantId]
    }
    
    /**
     * Get all Shopify variant IDs for a given SKU
     * @param sku 5-digit SKU
     * @return List of variant IDs that map to this SKU
     */
    fun getVariantIdsBySku(sku: String): List<String> {
        return shopifyVariantToSku.entries
            .filter { it.value == sku }
            .map { it.key }
    }
    
    /**
     * Get all known Shopify variant IDs
     * @return Set of all Shopify variant IDs
     */
    fun getAllVariantIds(): Set<String> {
        return shopifyVariantToSku.keys
    }
    
    /**
     * Get all known SKUs
     * @return Set of all 5-digit SKUs
     */
    fun getAllSkus(): Set<String> {
        return shopifyVariantToSku.values.toSet()
    }
    
    /**
     * Check if variant ID is known
     * @param variantId Shopify variant ID
     * @return true if mapping exists
     */
    fun hasVariantId(variantId: String): Boolean {
        return shopifyVariantToSku.containsKey(variantId)
    }
